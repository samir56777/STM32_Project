void Map_SetLevel(uint8_t level)
{
    current_level = level;
}

bool Map_IsWall(int row, int col)
{
    if (row < 0 || row >= MAP_HEIGHT || col < 0 || col >= MAP_WIDTH) {
        return true;
    }

    return mazes[current_level][row][col] == 1;
}

void Map_Draw(void)
{
    for (int row = 0; row < MAP_HEIGHT; row++) {
        for (int col = 0; col < MAP_WIDTH; col++) {
            int x = col * TILE_SIZE;
            int y = row * TILE_SIZE;

            if (mazes[current_level][row][col] == 1) {
                LCD_Draw_Rect(x, y, TILE_SIZE, TILE_SIZE, 31, 1);
            } else {
                LCD_Draw_Rect(x, y, TILE_SIZE, TILE_SIZE, 0, 1);
            }
        }
    }
}

uint8_t Map_GetLevel(void)
{
    return current_level;
}

bool Map_IsExit(int playerX, int playerY)
{
    int col = playerX / TILE_SIZE;
    int row = playerY / TILE_SIZE;

    return row == 18 && col == 18;
}